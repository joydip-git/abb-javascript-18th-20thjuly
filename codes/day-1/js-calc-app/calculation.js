function add(first, second) {
    var addRes = first + second
    return addRes
}
function subtract(first, second) {
    var subRes = first - second
    return subRes
}
function multiply(first, second) {
    var multiRes = first * second
    return multiRes
}
function divide(first, second) {
    var divRes = first / second
    return divRes
}